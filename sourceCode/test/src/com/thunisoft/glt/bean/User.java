package com.thunisoft.glt.bean;

public class User {

	private int id ;
	private String username;
	public int getId() {
		return id;
	}
	public User setId(int id) {
		this.id = id;
		return this;
	}
	public String getUsername() {
		return username;
	}
	public User setUsername(String username) {
		this.username = username;
		return this;
	}
	
	
	
	
}
